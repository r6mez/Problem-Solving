#define all(v)  v.begin(), v.end()

template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for (const auto& i : v) os << i << ' ';
    return os;
}

template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for (auto& i : v) is >> i;
    return is;
}

void solve() {
    int n; cin >> n;
    vi arr(n); cin >> arr;
    sort(all(arr));
    for (int realLieres = 0; realLieres <= n; ++realLieres){
        int liars = 0;
        for (int k = 0; k < n; ++k){
            liars += (realLieres < arr[k]);
        }
        if (liars == realLieres){
            cout << liars << "\n";
            return;
        }
    }
    cout << -1 << "\n";
}

