template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for(const auto& i : v) os << i << ' ';
    return os;
}
 
template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for(auto& i : v) is >> i;
    return is;
}

void solve(){
    int w, h, n; cin >> w >> h >> n;
    int xw = 0, yh = 0; 
    while (n--){
        int x, y, a; cin >> x >> y >> a;
        if(a == 1) xw = max(xw, x);
        else if (a == 2) w = min(w, x);
        else if (a == 3) yh = max(yh, y);
        else if (a == 4) h = min(h, y);
    }

    int width = max(0, w - xw);
    int height = max(0, h - yh);
    int area = width * height; 
    cout << area << "\n";
}

