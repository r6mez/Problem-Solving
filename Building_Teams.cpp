istream& operator>>(istream& is, vector<T>& v) {
    for (auto& i : v) is >> i;
    return is;
}

bool dfs(int node, int currentTeam, vi &teams, const vector<vi> &graph) {
    if (teams[node] != 0) {
        if(teams[node] == currentTeam) return true;
        else return false;
    }
    teams[node] = currentTeam;
    for (int neighbor : graph[node]) {
        if (!dfs(neighbor, 3 - currentTeam, teams, graph)) {
            return false;
        }
    }
    return true;
}

void solve() {
    int n, m;
    cin >> n >> m;
    vector<vi> graph(n);
    for (int i = 0; i < m; i++) {
        int x, y;
        cin >> x >> y;
        x--; y--;
        graph[x].push_back(y);
        graph[y].push_back(x);
    }

    vi teams(n, 0); 
    bool isPossible = true;

    for (int i = 0; i < n && isPossible; i++) {
        if (teams[i] == 0) { 
            isPossible = dfs(i, 1, teams, graph);
        }
    }

    if (!isPossible) {
        cout << "IMPOSSIBLE" << endl;
    } else {
        for (int i = 0; i < n; i++) {
            cout << teams[i] << " ";
        }
        cout << endl;
    }
}


