template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for (const auto& i : v) os << i << ' ';
    return os;
}

template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for (auto& i : v) is >> i;
    return is;
}

void solve() {
    int n; cin >> n;
    vector<int> a(n);
    long long sum = 0;
    int negs = 0;
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
        if (a[i] < 0) {
            ++negs;
            a[i] = -a[i];
        }
        sum += a[i];
    }
    sort(a.begin(), a.end());
    if (negs % 2 == 1) sum -= 2 * a[0];
    cout << sum << "\n";

}
