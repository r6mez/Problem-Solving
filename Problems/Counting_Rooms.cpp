template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for (const auto& i : v) os << i << ' ';
    return os;
}

template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for (auto& i : v) is >> i;
    return is;
}

void solve() {
    int n, m; cin >> n >> m;
    vector<string> x(n); cin >> x;
    int counter = 0;

    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            if (x[i][j] == '#') continue;

            counter++;
            
            stack<pair<int, int>> s;
            s.push({ i,j });
            while (!s.empty())
            {
                auto [h, u] = s.top(); s.pop();
                if (h < 0 || h >= n || u < 0 || u >= m || x[h][u] == '#') continue;
                else x[h][u] = '#';
                s.push({ h + 1, u });
                s.push({ h - 1, u });
                s.push({ h, u + 1 });
                s.push({ h, u - 1 });
            }
        }
    }

    cout << counter << "\n";
}

