template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for(const auto& i : v) os << i << ' ';
    return os;
}
 
template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for(auto& i : v) is >> i;
    return is;
}

void solve(){
    int n, t; cin >> n >> t;
    if(t == 1) {
        cout << n << "\n";
        return;
    }

    int counter = 0;
    int sqrtT = (sqrt(t-1));

    for (int i = 1; (i <= sqrtT && i <= n); ++i) {
        if ((t-1) % i == 0) {
            counter++;
            if (i != (t-1) / i && ((t-1)/i) <= n) {
                counter++;
            }
        }
    }
    cout << counter << "\n";
}

