template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for(const auto& i : v) os << i << ' ';
    return os;
}
 
template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for(auto& i : v) is >> i;
    return is;
}

void solve(){
    int n; cin >> n;
    int ones = 0, zeros = 0;
    vi a(n);
    for (int i = 0; i < n; i++){
        cin >> a[i];
        if(a[i]) ones++;
        else zeros++;
    }
    
    int maxOnes = INT_MIN;
    for (int i = 0; i < n; i++){
        for (int j = i; j < n; j++){
            int currentOnes = ones;
            for (int k = i; k <= j; k++){
                if(a[k] == 0) currentOnes++;
                else currentOnes--;
            }
            maxOnes = max(maxOnes, currentOnes);
        }
    }
    cout << maxOnes;
}

