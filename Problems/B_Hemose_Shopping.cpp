template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for(const auto& i : v) os << i << ' ';
    return os;
}
 
template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for(auto& i : v) is >> i;
    return is;
}

void solve(){
    int n, x; cin >> n >> x;
    vi a(n); cin >> a;
    vi b(a);
    sort(b.begin(), b.end());
    if (x*2 <= n){
        cout << "YES" << endl;
    } else {
        for (int i = 0; i < n; i++){
            if (a[i] != b[i] && max(n - 1 - i, i) < x) {
                cout << "NO" << endl;
                return;
            }
        }
        cout << "YES" << endl;
    }
}

/*
NOTES:
n = 5
x = 4
n - x - 1 = 0 
5 1 2 3 4
1 2 3 4 5

*/

int main() {
    ios_base::sync_with_stdio(false); 
    cin.tie(nullptr);
    int t = 1; 
    cin >> t;
    while (t--){
        solve();
    }
    return 0;
}

