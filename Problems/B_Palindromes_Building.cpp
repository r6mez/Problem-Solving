template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for(const auto& i : v) os << i << ' ';
    return os;
}
 
template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for(auto& i : v) is >> i;
    return is;
}

ull fact(ull n){
     return (n==0) || (n==1) ? 1 : n*fact(n-1);
}

void solve(){
    int n; string s; cin >> n >> s;

    map<char, int> freq;
    for (char c : s) freq[c]++;

    ull x = fact(n/2);
    ull y = 1;
    
    int odd = 0;
    for (auto& [c, f] : freq) {
        if(f%2 == 1) odd++;
        y *= fact(f / 2);
    }

    if((n%2 == 0 && odd == 0) || n%2 == 1 && odd == 1) {
        ull ways = x/y;
        cout << ways << "\n";
    } else {
        cout << 0 << "\n";
    }
}

