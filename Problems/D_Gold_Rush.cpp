bool isInt(double num) {
    if (abs(num - int(num)) == 0) return true;
    else return false;
}

bool isPossible(double num, double m){
    double x = (2.0 / 3) * num, y = (1.0 / 3) * num;
    if (x == m || y == m) return true;
    else if ((x < m && y < m) || !(isInt(x) && isInt(y))) return false;
    else return (isPossible(x, m) || isPossible(y,m));
}


void solve() {
    double n, m; cin >> n >> m;
    if(n == m){
        cout << "YES\n"; return;
    } else if (n < m) {
        cout << "NO\n"; return;
    } else {
        if (isPossible(n,m)) cout << "YES\n";
        else cout << "NO\n";
    }
}

