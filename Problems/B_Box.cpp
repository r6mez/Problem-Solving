template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for(const auto& i : v) os << i << ' ';
    return os;
}
 
template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for(auto& i : v) is >> i;
    return is;
}

void solve(){
    int n; cin >> n;
    vector <int> q(n), a(n);
    for (int &i : q)
        cin >> i;
 
    vector <bool> used(n);
    a[0] = q[0]; used[q[0] - 1] = true;
    for (int i = 1; i < n; i++) {
        if (q[i] <= i) {
            cout << "-1\n"; return;
        }
        if (q[i - 1] < q[i]) {
            a[i] = q[i];
            used[q[i] - 1] = true;
        }
    }
 
    int it = 0;
    for (int i = 0; i < n; i++) {
        if (a[i] == 0) {
            while (used[it])
                it++;
            a[i] = it + 1;
            used[it] = true;
        }
    }
 
    for (int i : a)
        cout << i << ' ';
    cout << '\n';
}

