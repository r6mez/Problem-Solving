const int MOD = 998244353, INF = 2e18;

const int N = 3e5 + 100;
int dp[N], L[N];

int calc(int i) {
    if (i < 0) return 0;
    if (i == 0) return 1;
    if (dp[i] != -1) return dp[i];
    return dp[i] = ((2 * calc(i - 1))%MOD - calc(L[i] - 1)%MOD + MOD)%MOD;
}

void solve() {
    memset(dp, -1, sizeof(dp));
    memset(L, 0, sizeof(L));
    int n, m; cin >> n >> m;

    for (int i = 0; i < m; i++){
        int l, r; cin >> l >> r;
        L[r] = max(L[r], l);
    }

    for (int i = 1; i <= n; i++){
        L[i] = max(L[i], L[i - 1]);
    }
    
    int answer = (2 * ((calc(n - 1)%MOD - calc(L[n] - 1)%MOD + MOD)%MOD))%MOD;
    cout << answer << "\n";
}

