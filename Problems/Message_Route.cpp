template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for (const auto& i : v) os << i << ' ';
    return os;
}

template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for (auto& i : v) is >> i;
    return is;
}

void solve() {
    int n, m; cin >> n >> m;
    vector<vector<int>> graph(n + 1);
    for (int i = 0; i < m; i++) {
        int u, v; cin >> u >> v;
        graph[u].push_back(v);
        graph[v].push_back(u);
    }

    vector<bool> visited(n + 1);
    visited[1] = true;
    vector<int> parents(n + 1);
    queue<int> q;
    q.push(1);
    while (!q.empty()) {
        int s = q.front(); q.pop();
        for (auto u : graph[s]) {
            if (visited[u]) continue;
            visited[u] = true;
            q.push(u);
            parents[u] = s; 
        }
    }

    if(parents[n] == 0) {
        cout << "IMPOSSIBLE" << "\n";
        return;
    }

    stack<int> path;
    path.push(n);
    while (path.top() != 1){
        path.push(parents[path.top()]);
    }

    cout << path.size() << "\n";
    while(!path.empty()) {
        cout << path.top() << " ";
        path.pop();
    }
}

