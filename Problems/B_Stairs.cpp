template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for(const auto& i : v) os << i << ' ';
    return os;
}
 
template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for(auto& i : v) is >> i;
    return is;
}

void solve(){
    ll n; cin >> n;
    int counter = 0;
    for (ll i = 1; i <= n; i+=i+1){
        ll currentStairBlocks = (i*(i+1)/2);
        if(n - currentStairBlocks >= 0){
            counter++;
            n -= currentStairBlocks;
        } else {
            break;
        }
    }
    cout << counter << "\n";
    
}

