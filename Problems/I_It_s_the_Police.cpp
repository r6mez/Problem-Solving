#define all(v)  v.begin(), v.end()
#define search(vec, item) find(all(vec), item) != vec.end()
 
template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for(const auto& i : v) os << i << ' ';
    return os;
}
 
template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for(auto& i : v) is >> i;
    return is;
}

void solve(){
    int n, m; cin >> n >> m;
    vector<vi> graph(n);

    for (int i = 0; i < m; i++)
    {
        int a, b; cin >> a >> b;
        a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    int minNodeIndex = -1, minNodeChilderen = INT_MAX;
    for (int i = 0; i < n; i++){
        if(minNodeChilderen > graph[i].size()){
            minNodeChilderen = graph[i].size();
            minNodeIndex = i;
        }
    }
    

    for (int i = 0; i < n; i++)
    {
        if(search(graph[i], minNodeIndex) || i == minNodeIndex)
            cout << "0 ";
        else cout << "1 ";
    }
}

/*
NOTES:
A policeman, being set on some square, can observe 
this square and also all squares connected with it.
- at least one square is not observed
- set policemen to as many squares as possible.


select the choosen square 
select other squares so that it doesn't select this square
*/

int main() {
    ios_base::sync_with_stdio(false); 
    cin.tie(nullptr);
    int t = 1; 
    // cin >> t;
    while (t--){
        solve();
    }
    return 0;
}

