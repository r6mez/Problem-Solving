void UseFile() { freopen("file.in", "r", stdin); freopen("file.out", "w", stdout); }
const int MOD = 1000000007, INF = 2e18;

void solve() {
    int n, m, k; cin >> n >> m >> k;
    vi a(n); for(int &x : a) cin >> x, x--;
    vector<vi> adj(n);
    for (int i = 0; i < m; i++){
        int u, v; cin >> u >> v; u--; v--;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }

    vi dis(k), vis(n), level(n);
    queue<int> q;
    q.push(0);
    vis[0] = true;
    level[0] = 0;

    while(q.size()){
        int u = q.front(); q.pop();

        dis[a[u]] = max(dis[a[u]], level[u]);
        
        for(int v : adj[u]){
            if(vis[v]) continue;
            vis[v] = true;
            level[v] = level[u] + 1;
            q.push(v);
        }
    }

    cout << dis << "\n";
}

