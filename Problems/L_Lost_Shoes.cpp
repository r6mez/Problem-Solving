template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for (const auto& i : v) os << i << ' ';
    return os;
}

template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for (auto& i : v) is >> i;
    return is;
}

void solve() {
    int n; cin >> n;
    vector<int> r(n), l(n); cin >> r >> l;

    vector<int> right_pos(n + 1), left_pos(n + 1);
    for (int i = 0; i < n; ++i) {
        right_pos[r[i]] = i;
        left_pos[l[i]] = i;
    }

    int swaps = 0;
    vector<bool> visited(n, false);
    for (int i = 0; i < n; ++i) {
        if (!visited[i]) {
            int cycle_length = 0;
            int x = i;
            while (!visited[x]) {
                visited[x] = true;
                x = right_pos[x + 1];
                ++cycle_length;
            }
            if (cycle_length > 1) {
                swaps += (cycle_length - 1);
            }
        }
    }

    fill(visited.begin(), visited.end(), false);
    for (int i = 0; i < n; ++i) {
        if (!visited[i]) {
            int cycle_length = 0;
            int x = i;
            while (!visited[x]) {
                visited[x] = true;
                x = left_pos[x + 1];
                ++cycle_length;
            }
            if (cycle_length > 1) {
                swaps += (cycle_length - 1);
            }
        }
    }

    cout << swaps << "\n";
}
