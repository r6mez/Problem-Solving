void UseFile() { freopen("file.in", "r", stdin); freopen("file.out", "w", stdout); }
const int MOD = 1000000007, INF = 2e18;

void solve() {
    string s; cin >> s;
    int n = s.size();
    int zeros = 0, ones = 0;
    for(char c : s){
        if(c == '0') zeros++;
        else ones++;
    }
    
    if(ones == zeros) finish(0);

    for (int i = 0; i < n; i++){
        if(s[i] == '0') {
            if(ones) ones--;
            else finish(ones + zeros);
        } else {
            if(zeros) zeros--;
            else finish(ones + zeros);
        }
    }
}

