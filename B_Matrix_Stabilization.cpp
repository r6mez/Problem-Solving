template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for (const auto& i : v) os << i << ' ';
    return os;
}

template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for (auto& i : v) is >> i;
    return is;
}

void solve() {
    int n, m; cin >> n >> m;
    int a[n][m];
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            cin >> a[i][j];
        }
    }

    bool flag = true;
    while (flag) {
        bool match = false;
        for (int i = 0; i < n; i++)
        {
            for (int j = 0; j < m; j++)
            {
                int maxNighbour = 0;
                if (i != 0) maxNighbour = max(maxNighbour, a[i - 1][j]);
                if (i != n - 1) maxNighbour = max(maxNighbour, a[i + 1][j]);
                if (j != 0) maxNighbour = max(maxNighbour, a[i][j - 1]);
                if (j != m - 1) maxNighbour = max(maxNighbour, a[i][j + 1]);

                if (a[i][j] > maxNighbour) {
                    a[i][j] = maxNighbour;
                    match = true;
                }
            }
        }
        if (match == false) flag = false;
    }

    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            cout << a[i][j] << " ";
        } cout << "\n";
    }
}

