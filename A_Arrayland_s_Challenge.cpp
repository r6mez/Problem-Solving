#define all(v)  v.begin(), v.end()

template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for (const auto& i : v) os << i << ' ';
    return os;
}

template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for (auto& i : v) is >> i;
    return is;
}

void solve() {
    int n; cin >> n;
    vi a(n); cin >> a;
    int q; cin >> q;
    while (q--) {
        int l, r; cin >> l >> r;
        vi subarray(a.begin() + l - 1, a.begin() + r);
        sort(all(subarray));

        int minDiff = INT_MAX;
        for (int j = 1; j < subarray.size(); ++j) {
            minDiff = min(minDiff, subarray[j] - subarray[j - 1]);
            if(minDiff == 0) break;
        }

        cout << minDiff << "\n";
    }
}

