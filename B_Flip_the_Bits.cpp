template<typename T>
ostream& operator<<(ostream& os, const vector<T>& v) {
    for(const auto& i : v) os << i << ' ';
    return os;
}
 
template<typename T>
istream& operator>>(istream& is, vector<T>& v) {
    for(auto& i : v) is >> i;
    return is;
}

void solve(){
    int n; cin >> n;
    string a, b; cin >> a >> b;

    a.push_back('0');
    b.push_back('0');

    vi prefixSum(n, 0);
    if(a[0] == '0') prefixSum[0] = -1;
    else prefixSum[0] = 1;

    for (int i = 1; i < n; i++){
        if(a[i] == '0') prefixSum[i] = prefixSum[i-1] - 1;
        else prefixSum[i] = prefixSum[i-1] + 1;
    }

    for (int i = 0; i < n; i++)
    {
        if((a[i] == b[i] && a[i+1] != b[i+1]) || (a[i] != b[i] && a[i+1] == b[i+1])){
            if(prefixSum[i] != 0){
                cout << "NO\n";
                return;
            }
        }
    }
    cout << "YES\n";
}

