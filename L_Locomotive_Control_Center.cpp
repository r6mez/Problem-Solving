void solve() {
    int n; cin >> n;
    deque<int> a, b ,c;
    for (int i = 0; i < n; i++) {
        int car; cin >> car;
        a.push_front(car);
    }

    int nextCar = 1;
    vector<string> op;
    while(!a.empty() || !b.empty()){
        if(a.front() == nextCar){
            c.push_back(a.front());
            a.pop_front();
            op.push_back("A C");
            nextCar++;
        } else if (b.front() == nextCar){
            c.push_back(b.front());
            b.pop_front();
            op.push_back("B C");
            nextCar++;
        } else {
            b.push_front(a.front());
            a.pop_front();
            op.push_back("A B");
        }

        if(a.empty() && !b.empty() && b.front() != nextCar){
            cout << -1 << "\n";
            return;
        }
    }
    
    cout << op.size() << "\n";
    for (int i = 0; i < op.size(); i++){
        cout << op[i] << "\n";
    }
    
}

