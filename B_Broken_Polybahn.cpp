void solve() {
    int n;
    cin >> n;

    vector<vi> adj(n);
    int ans = 0;
    for (int i = 0; i < n - 1; ++i) {
        int u, v;
        cin >> u >> v;
        u--; v--;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }
    int mod = 1e9 + 7;
    for (int i = 0; i < n; ++i) {
        ans += binpow(2, adj[i].size(), mod) - 1;
        ans = (ans % mod + mod) % mod;
    }

    ans -= (n - 1);
    if (ans < 0)ans += mod;

    cout << ans;
}

